    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        // TODO add your handling code here:
        ManutencoesDAO manExclui = new ManutencoesDAO();

        int selectedRow = controleManutencao.getSelectedRow();
        if (selectedRow != -1 && selectedRow < controleManutencao.getRowCount()) {
            Object value = controleManutencao.getValueAt(selectedRow, 0);

            if (value != null && value instanceof Integer) {
                int id = (int) value;
                manExclui.removeById(id);
                povoarJtable();
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma manutenção para excluir");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma manutenção para excluir");
        }

    }//GEN-LAST:event_jButton7MouseClicked

    private void jButton8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton8MouseClicked
        ManutencoesDAO manFinaliza = new ManutencoesDAO();
        float horas = 0.0f;

        int selectedRow = controleManutencao.getSelectedRow();
        if (selectedRow != -1 && selectedRow < controleManutencao.getRowCount()) {
            Object value = controleManutencao.getValueAt(selectedRow, 0);

            if (value != null && value instanceof Integer) {
                int id = (int) value;
                horas = Float.parseFloat(JOptionPane.showInputDialog("Informe a quantidade de horas trabalhadas"));
                manFinaliza.updateData_fim(id, horas);
                povoarJtable();
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma manutenção para finalizar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma manutenção para finalizar");
        }
    }//GEN-LAST:event_jButton8MouseClicked


