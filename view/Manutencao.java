    public void povoarJtable() {
        //criação de uma variavel modelo da tabela
        DefaultTableModel modelo = (DefaultTableModel) controleManutencao.getModel();
        //abre a tabela com 0 linhas quando n houver itens
        modelo.setNumRows(0);

        ManutencoesDAO mdao = new ManutencoesDAO();

        ManutencoesDAO manutencaodao = new ManutencoesDAO();
        //enquanto tiver alguma coisa na busca do get ele vai rpetir esse comando
        for (model.Manutencao m : mdao.getManutencao()) {
            modelo.addRow(new Object[]{
                m.getId(),
                m.getDescricao(),
                m.getLocalizacao(),
                m.getObservacao(),
                m.getData_inicio(),
                m.getData_fim(),
                m.getStatus(),
                m.getHorasdeclaradas()

            });
        }

    }

