    public void povoarJtable() {
        //criação de uma variavel modelo da tabela
        DefaultTableModel modelo = (DefaultTableModel) tabelaOrdem.getModel();
        //abre a tabela com 0 linhas quando n houver itens
        modelo.setNumRows(0);

        Ordens_de_producaoDAO odao = new Ordens_de_producaoDAO();

        Ordens_de_producaoDAO ordens_de_producaodao = new Ordens_de_producaoDAO();
        //enquanto tiver alguma coisa na busca do get ele vai rpetir esse comando
        for (model.Ordem_de_producao o : odao.getOrdem_de_producaos()) {
            modelo.addRow(new Object[]{
                o.getId(),
                o.getModelo(),
                o.getMarca(),
                o.getCor(),
                o.getStatus(),
                o.getSetor_atual()
            });
        }

    }

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked

        int selectedRow = tabelaOrdem.getSelectedRow();
        MovimentaOrdem mo = new MovimentaOrdem();
        if (selectedRow != -1 && selectedRow < tabelaOrdem.getRowCount()) {
            Object value = tabelaOrdem.getValueAt(selectedRow, 0);

            if (value != null && value instanceof Integer) {
                int id = (int) value;
                manda.id = id;
                mo.setVisible(true);
                povoarJtable();
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma manutenção para excluir");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma manutenção para excluir");
        }


    }//GEN-LAST:event_jButton2MouseClicked

