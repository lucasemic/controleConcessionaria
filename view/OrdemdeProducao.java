    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked

        Ordens_de_producaoDAO update = new Ordens_de_producaoDAO();
        int selectedRow = tabelaOrdem.getSelectedRow();

        if (selectedRow != -1 && selectedRow < tabelaOrdem.getRowCount()) {
            Object value = tabelaOrdem.getValueAt(selectedRow, 0);

            if (value != null && value instanceof Integer) {
                int valor = (int) value;
                int opc = JOptionPane.showConfirmDialog(null, "Deseja Continuar");
                update.updateFinalizacao(valor);
                povoarJtable();
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma manutenção para excluir");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma manutenção para excluir");
        }
       
        // o comando getValueAt vai retornar a linha selecionada na tabela pelo user, e o getSelected é que vai informar a linha selecionada

        povoarJtable();
    }//GEN-LAST:event_jButton3MouseClicked

